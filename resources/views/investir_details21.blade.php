            Le projet de création d’un complexe charbonnier à Salkadamna s’inscrit dans le cadre de la politique énergétique du gouvernement du Niger qui a pour but d’accroitre la capacité énergétique du pays afin d’aboutir à une indépendance énergétique. Le projet vise dans un premier volet à pallier l’accès limité de la population nigérienne aux énergies modernes et de répondre à une demande croissante en énergie qui est estimée à 2615 GWH en 2020 et 6434 GWH en 2030 par rapport à 1557 GWH en 2015. Le site minier de Salkadamna est doté d’une capacité de production annuelle de 1,1 millions de tonnes de charbon (lignite) quant aux réserves actuelles elles sont évaluées à 69 millions de tonnes. L’énergie produite de la centrale thermique sera de 200 MW en quatre tranches de 50 MW et la transmission se fera sur une ligne 330 KV sur environ 400 KM entre Salkadamna et Niamey et une autre ligne de132 KV sur environ 200 KM entre Salkadamna – Tahoua – Malbaza. Dans un second volet il s’agirait de fabriquer des briquettes de charbon minéral pour usage domestique en substitution au bois pour atténuer la déforestation.
            <br></br>
            <b>Bénéfices</b><br>
            <ul style="margin: 15px 15px;">
              <li>Augmentation de la capacité de production d’électricité pour répondre aux besoins de la population nigérienne ;
              </li>
              <li>Exportation de l’électricité vers les pays voisins du Niger à travers le WAP ; </li>
              <li>Augmentation de la production nationale en énergie pour réduire la dépendance de l’extérieur ; </li>
              <li>Usage des briquettes par les ménages comme substitut au bois ;</li>
              <li>Plusieurs avantages fiscaux accordés par l’Etat dans le secteur minier et énergétique.</li>
            </ul>
